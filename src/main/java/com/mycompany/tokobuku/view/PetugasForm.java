    public void loadDB(){
        try{
            PetugasDAOImp petugasDAO = new PetugasDAOImp(){};
            List<Petugas> listPetugas=petugasDAO.list();
            DefaultTableModel DFT = (DefaultTableModel)tablePetugas.getModel();
            DFT.setRowCount(0);
            for(Petugas petugas: listPetugas){
                int id_akun=petugas.getIdAkun();
                String username = petugas.getUsername();
                String password=petugas.getPassword();
                String status=petugas.getStatus();
                DFT.addRow(new Object[]{id_akun,username, status});
            }
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
    }

    public void loadDBByUsername(String username){
        try{
            PetugasDAOImp petugasDAO = new PetugasDAOImp(){};
            List<Petugas> listPetugas=petugasDAO.listByUsername(username);
            DefaultTableModel DFT = (DefaultTableModel)tablePetugas.getModel();
            DFT.setRowCount(0);
            for(Petugas petugas: listPetugas){
                int id_akun=petugas.getIdAkun();
                String usernameAkun = petugas.getUsername();
                String password=petugas.getPassword();
                String status=petugas.getStatus();
                DFT.addRow(new Object[]{id_akun,usernameAkun, status});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

