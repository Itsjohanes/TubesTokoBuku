    private void tablePetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePetugasMouseClicked
        try{
            int row=tablePetugas.getSelectedRow();
            String dataKlik=(tablePetugas).getModel().getValueAt(row, 0).toString();
            int id_akun = Integer.parseInt(dataKlik);

            PetugasDAOImp dao=new PetugasDAOImp() {};
            Petugas petugasDAO=dao.getPetugas(id_akun);

            txtIdAkun.setText(String.valueOf(petugasDAO.getIdAkun()));
            txtUsername.setText(String.valueOf(petugasDAO.getUsername()));
            txtPass.setText(String.valueOf(petugasDAO.getPassword()));
            //String status_akun=String.valueOf(petugasDAO.getStatus());

            if(String.valueOf(petugasDAO.getStatus()).equals("Supervisor")){
                rbSupervisor.setSelected(true);
            }else{
                rbStaff.setSelected(true);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tablePetugasMouseClicked

