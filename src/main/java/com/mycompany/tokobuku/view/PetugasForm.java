    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try{
           int id_akun=Integer.parseInt(txtIdAkun.getText());
            String username=txtUsername.getText();

            int pesan=JOptionPane.showOptionDialog(this, "Anda yakin akan menghapus akun username= "+username+"?", "Konfirmasi Hapus Akun Petugas", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if(pesan==JOptionPane.YES_OPTION){
                Petugas petugas=new Petugas();
                petugas.setIdAkun(id_akun);
                PetugasDAOImp dao=new PetugasDAOImp() {};
                dao.delete(petugas);
                loadDB();;
                clearTextField();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

