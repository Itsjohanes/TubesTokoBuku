            int pesan=JOptionPane.showOptionDialog(this, "Anda yakin akan menghapus nama penerbit:"+namaPenerbit+"?", "Konfirmasi Hapus Akun Petugas", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
