    public void clear(){
        txtid.setText("");
        txtnama.setText("");
        txtemail.setText("");
        txtid.setEditable(true);

        showTable();
    }

    public void showTable(){
        try{
        PenerbitDAOImp penerbit = new PenerbitDAOImp();
        List<Penerbit> listpenerbit = penerbit.list();
        DefaultTableModel DFT = (DefaultTableModel) tblpenerbit.getModel();
        DFT.setRowCount(0);
        for(Penerbit penerbits : listpenerbit){
            int idPenerbit = penerbits.getIdPenerbit();
            String namaPenerbit = penerbits.getNamaPenerbit();
            String email = penerbits.getEmail();
           
            DFT.addRow(new Object[]{idPenerbit,namaPenerbit,email});
              
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    } 

    public void loadDBByNama(String nama){
        try{
            PenerbitDAOImp penerbitDAO = new  PenerbitDAOImp(){};
            List<Penerbit> listPenerbit = penerbitDAO.listByNamaPenerbit(nama);
            DefaultTableModel DFT = (DefaultTableModel)tblpenerbit.getModel();
            DFT.setRowCount(0);
          for(Penerbit penerbits : listPenerbit){
            int idPenerbit = penerbits.getIdPenerbit();
            String namaPenerbit = penerbits.getNamaPenerbit();
            String email = penerbits.getEmail();
           
            DFT.addRow(new Object[]{idPenerbit,namaPenerbit,email});
              
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }

